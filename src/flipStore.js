import gsap from "gsap";
import { Flip } from "gsap/Flip";

gsap.registerPlugin(Flip);

let flipState = null;

export function captureFlipState(targets = ".flip") {
    flipState = Flip.getState(targets);
}

export function runFlipAnimation(targets = ".flip", ease = "power1.out", duration = 0.5) {
    if (flipState) {
        Flip.from(flipState, {
            targets,
            duration: duration,
            ease: ease
        });
        flipState = null;
    }
}
